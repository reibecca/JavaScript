// EXERCICE 01 - CALCULATOR

function calculate (num1, num2, operator){
    if (operator === '+'){
        console.log(num1 + num2)
    }
    if (operator === '-'){
        console.log(num1 - num2)
    }
    if (operator === '*'){
        console.log(num1 * num2)
    }
    if (operator === '-'){
        console.log(num1 + num2)
    }
    if (operator === '%'){
        console.log(num1 % num2)
    }
}
calculate(5, 4, '%')
